export type EmojiCategory = (typeof EMOJI_CATEGORIES)[number];
export type EmojiCategoryId = EmojiCategory['id'];

export const ARENA_EMOJIS = EMOJI_CATEGORIES.flatMap((c) => c.emojis) as readonly string[];

export type ArenaEmoji = string;

const RECENT_KEY = 'lanista-arena-recent-emojis';
const MAX_RECENT = 16;

export function getRecentEmojis(): ArenaEmoji[] {
  try {
    const raw = localStorage.getItem(RECENT_KEY);
    if (!raw) return [];
    return (JSON.parse(raw) as string[]).slice(0, MAX_RECENT);
  } catch {
    return [];
  }
}

export function pushRecentEmoji(emoji: ArenaEmoji): void {
  const recent = getRecentEmojis();
  const next = [emoji, ...recent.filter((e) => e !== emoji)].slice(0, MAX_RECENT);
  try {
    localStorage.setItem(RECENT_KEY, JSON.stringify(next));
  } catch {
    // ignore
  }
}
